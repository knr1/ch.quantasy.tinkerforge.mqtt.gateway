package ch.quantasy.gateway.message.intent.LCD20x4;

import ch.quantasy.gateway.message.annotations.AValidator;
import ch.quantasy.gateway.message.annotations.Range;
import ch.quantasy.gateway.message.annotations.StringSize;

/**
 *
 * @author reto
 */
public class DeviceDefaultText extends AValidator implements Comparable<DeviceDefaultText> {

    @Range(from = 0, to = 3)
    private short line;
    @StringSize(max = 20)
    private String text;

    public DeviceDefaultText() {
    }

    public DeviceDefaultText(short line, String text) {
        this.line = line;
        this.text = text;
    }

    public short getLine() {
        return line;
    }

    public String getText() {
        return text;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 67 * hash + this.line;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final DeviceDefaultText other = (DeviceDefaultText) obj;
        if (this.line != other.line) {
            return false;
        }
        return true;
    }

    @Override
    public int compareTo(DeviceDefaultText o) {
        return this.line - o.line;
    }

}
